<h1 align="center">La invitación de la señora araña</h1>	
<p>
Un día, la señora araña decidió hacer una fiesta. 
</p>
<p>
	- Yo invito a mi casa a los animales más pequeños
del bosque - dijo la señora Araña.
</p>
<p>
	La invitación fue aceptada por los animales sin
ninguna clase de problemas. Aprovechando sus
ocho patas, la señora Araña escribió con mucho
cuidado las invitaciones para la fiesta. Además,
limpió y arregló su casa. La señora Araña sabía que
todos sus invitados eran muy puntuales, por eso cuando había pasado la hora y ningún
invitado apareció, ella se puso a llorar.
</p>
<p>
	Nadie quiere venir a mi casa - se decía entre sollozos.
</p>
<p>
	Entonces decidió salir a averiguar qué había pasado y encontró que sus invitados
esperaban afuera de su puerta en una larga fila. Todos miraban con enojo un letrero que
había sobre la puerta “Antes de entrar límpiese los pies”.
Todos esperaban al señor Ciempiés, quien con mucha paciencia limpiaba cada uno de sus
pies antes de entrar a la casa de la señora Araña.
Felizmente el señor Ciempiés terminó su tarea y todos los invitados pudieron entrar a la
casa y participar en la fiesta, que estuvo de lo más entretenida.
</p>
